"""![plaza se mueve](https://media0.giphy.com/media/ZAzlopoHETs5lZQ6EZ/giphy.gif "plaza")

Para realizar este ejercicio utilizaremos un m√©todo super b√°sico para la eliminaci√≥n de fondo. Para esto sigue la siguiente receta:
 1. Pase a escala de grises todas las im√°genes secuenciales.
 2. Almacene en una variable la resta de las im√°genes consecutivas.

$$ imagen\_out = imagen(t) - imagen(t+1) $$

3. Establezca un umbral para eliminar algunos artefactos. Pruebe con al menos 3 umbrales.

$$ imagen\_out = imagen\_out> Umbral $$

**Nota**: No es necesario que construya una clase para esta parte.
"""

def dect_mov(sec_img, umbral=100):
    """
    Detecta movimiento en una secuencia de im√°genes RGB usando diferencia de fotogramas.

    Parameters
    ----------
    sec_img : ndarray
        Secuencia de im√°genes RGB (shape: N, alto, ancho, 3)
    umbral : int
        Valor m√≠nimo de diferencia para considerar que hay movimiento.

    Returns
    -------
    List of ndarray
        Lista de im√°genes binarias (1: cambio detectado, 0: fondo).
    """
    lib = LibImagen()  # Instanciamos la clase

    movs = []

    for i in range(len(sec_img) - 1):
        # Convertir ambas im√°genes a escala de grises usando tu clase
        gray1 = lib.to_gray(Imagen(sec_img[i])).imagen[:, :, 0]
        gray2 = lib.to_gray(Imagen(sec_img[i + 1])).imagen[:, :, 0]

        # Calcular diferencia absoluta
        imagen_out = np.abs(gray1 - gray2)

        # Aplicar umbral
        movimiento = (imagen_out > umbral).astype(np.uint8) * 255

        movs.append(movimiento)

    return movs

